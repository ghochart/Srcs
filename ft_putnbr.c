void    ft_putnbr(int n)
{
  if (nb < 0)
  {
    ft_putchar('-');
    nb = -nb;
  }
  if (n > 10)
  {
    ft_putnbr(n / 10);
    ft_putnbr(n % 10);
  }
  else
    ft_putchar(48 + n);
}
